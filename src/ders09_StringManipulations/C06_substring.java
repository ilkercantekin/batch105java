package ders09_StringManipulations;

public class C06_substring {
    public static void main(String[] args) {
        String  str=" Java öğren işi kap";
        System.out.println(str.substring(5));  ///ogren, işi kap
        System.out.println(str.substring(0));// Java öğren işi kap hepsini yazar 0.indexten itibaren
        System.out.println(str.length()); //19 yazar

    }
}
